    private void ingresarFecha() {
        int anio = sistema.getFechaSistema().get(Calendar.YEAR);
        int mes = sistema.getFechaSistema().get(Calendar.MONTH) + 1;
        int dia = sistema.getFechaSistema().get(Calendar.DAY_OF_MONTH);
        String fecha = String.valueOf(dia) + "/" + String.valueOf(mes) + "/" + String.valueOf(anio);
        lblFecha.setText(fecha);
    }
